#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;
	t_list	*next;

	if (!lst || !del)
		return ;
	temp = *lst;
	*lst = NULL;
	while (temp != NULL)
	{
		next = temp->next;
		ft_lstdelone(temp, del);
		temp = next;
	}
}
